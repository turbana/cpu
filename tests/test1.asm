;;	Assembler Test
;;
;;	These binary values must match the entire .o file
;;
;;	Magic Header Word 1		; 1101 1110 1010 1101 |
;;	Magic Header Word 2		; 1111 0000 0000 1101 |
;;	Data chunk count		; 0000 0000 0000 0000 |
;;	Instruction chunk count		; 0000 0000 0000 0001 |
;;	 \ Chunk address		; 0000 0000 0000 0000 |
;;	 \ Chunk length			; 0000 0000 0010 1111 |
start:	ldw	$1, b10110($2)		; 1100 0101 1001 0001 |
	stw	(end-24)($0), $4	; 1110 1001 1100 0100 |
	jmp	end			; 1011 1000 0010 0100 |
	add	$1, $2, $3		; 0000 0001 0001 0001 |
	add	$6, $7, 8		; 0000 0011 1111 1110 |
	sub	$3, $0, $1		; 0000 1000 1000 0011 |
	sub	$7, $5, -2		; 0000 1111 0110 0010 |
	and	$2, $4, $0		; 0001 0000 0010 0010 |
	and	$7, $1, -7		; 0001 0100 1100 1011 |
	or	$3, $1, $4		; 0001 1010 0000 1011 |
	or	$4, $1, 1		; 0001 1000 1100 1100 |
	s.eq	$1, $4			; 0010 0010 0000 1000 |
	s.ne	$4, 5			; 0010 0010 1110 0001 |
	s.lt	$5, -1			; 0010 0111 1110 1100 |
	s.lte	$4, $0			; 0010 0000 0010 0101 |
	s.gt	$3, $7			; 0010 0011 1001 1010 |
	s.gte	$7, -8			; 0010 0100 0111 1011 |
	s.ult	$3, 8			; 0010 0011 1101 1110 |
	s.ulte	$4, $6			; 0010 0011 0010 0111 |
	as.z	$1, $3, $5		; 0010 1010 1001 1001 |
	as.z	$4, $7, 8		; 0010 1011 1111 1100 |
	as.nz	$0, $0, $0		; 0011 0000 0000 0000 |
	as.nz	$1, $5, -2		; 0011 0111 0110 1001 |
	xor	$3, $5, (010/4)		; 0011 1010 0110 1011 |
	lui	$6, 0x0F		; 1010 0000 0111 1110 |
	addi	$3, 0xF0		; 1010 1111 1000 0011 |
	ldw	$3, $4($1)		; 0101 0010 0000 1011 |
	ldw	$5, 8($3)		; 0101 0011 1101 1101 |
	stw	$4($1), $2		; 0101 1010 0000 1010 |
	stw	-1($1), $5		; 0101 1111 1100 1101 |
	shl	$7, $4, $1		; 0100 0000 1010 0111 |
	shl	$3, $1, 8		; 0100 0100 0100 1011 |
	shr	$1, $6, $7		; 0100 1011 1011 0001 |
	shr	$7, $2, 1		; 0100 1000 1101 0111 |
	sar	$1, $6, 3		; 1001 1001 1111 0001 |
	inb	$1, $6			; 0110 0000 0011 0001 |
	outb	$4, $4			; 0110 1000 0010 0100 |
	trap	b1001			; 1011 0010 0100 0000 |
	jmp	$7			; 1001 0000 0011 1000 |
	jmp	$epc			; 1001 0000 0100 0000 |
end:	jmp	start			; 1011 1111 1101 0111 |
	lcr	$1, $cr1		; 1000 0000 0000 1001 |
	scr	$cr0, $3		; 1000 1000 0001 1000 |
	ldiw	$1, $2			; 0111 0000 0001 0001 |
	stiw	$7, $3			; 0111 1000 0001 1111 |
	jmp	0			; 1011 1111 1111 1111 |
	add	$0, $0, $0		; 0000 0000 0000 0000 |
