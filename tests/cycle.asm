	.data
	.word	0, 1, 2, 3

	.text
start:	lui	$1, 0		; .ldi	$1, 1		|@|  4 ($1=0000)
	addi	$1, 1		;			|@|  5 ($1=0001)
	lui	$2, 0		; .ldi	$2, 2		|@|  6 ($2=0000)
	addi	$2, 2		;			|@|  7 ($2=0002)
	lui	$3, 0		; .ldi	$3, 3		|@|  8 ($3=0000)
	addi	$3, 3		;			|@|  9 ($3=0003)
	ldw	$4, 0($3)	;			|@| 10 ($4=0003)
	s.eq	$3, $4		;			|@| 12 ($cr0=0009)
	jmp	start		;
	ldw	$4, 0($2)	;			|@| 14 ($4=0002)
	s.eq	$3, $4		;			|@| 16 ($cr0=000B)
	jmp	done		;			|@| 17 ($cr0=000D)
	jmp	start		;
done:	add	$4, $3, $1	;			|@| 20 ($4=0004)
	jmp	0		;			|@| 21 ($cr0=000E)
